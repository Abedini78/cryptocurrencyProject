function calculate_rsi(coin_ohlc){
    
    var avg_closegain=0
    var avg_closeloss=0
    var increaser_gain=0
    var increaser_loss=0
    var lastClosePrice=coin_ohlc[0][4]
     for(var l=1;l<84;l++){
         if(coin_ohlc[l][4]>lastClosePrice){
             increaser_gain=increaser_gain+1
             avg_closegain=avg_closegain+(coin_ohlc[l][4]-lastClosePrice)
             lastClosePrice=coin_ohlc[l][4]
             
         }
         if(coin_ohlc[l][4]<lastClosePrice){
            increaser_loss=increaser_loss+1
            avg_closeloss=avg_closeloss+(lastClosePrice-coin_ohlc[l][4])
            lastClosePrice=coin_ohlc[l][4]

        }
        

     }
     rs=(avg_closegain/increaser_gain)/(avg_closeloss/increaser_loss)
     rsi=100-100/(1+rs)
     
    return rsi
    
} 

